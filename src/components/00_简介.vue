// （2）有利于发现错误
// （3）更好的 IDE 支持，做到语法提示和自动补全
// （4）提供代码文档
// （5）有助于代码重构

// 5.静态类型的缺点
// （1）丧失了动态类型的代码灵活性
// （2）增加了编程工作量
// （3）更高的学习成本
// （4）引入了独立的编译步骤
// （5）兼容性问题
